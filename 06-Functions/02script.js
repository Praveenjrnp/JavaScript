// Return values & early returns(ise hamne if, else wale me dekha tha) ___________________________________________________________________________________________

// return means jaha se aaya hai, vhi vapas rakh denge

function abcd(){
    return 12;
}

console.log(abcd()); // Output: 12 (the function returns the value 12)


function abc(val){
    return 12 + val;
}

console.log(abc(2)); // Output: 14